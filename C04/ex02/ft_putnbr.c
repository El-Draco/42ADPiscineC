#include <unistd.h>

void	ft_putnbr(int nb)
{
	char temp;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
	{
		temp = nb % 10 + 48;
		ft_putnbr(nb / 10);
		write(1, &temp, 1);
	}
	if (nb < 10)
	{
		temp = nb % 10 + 48;
		write(1, &temp, 1);
	}
}
