int		ft_is_prime(int nb)
{
	int i;
	int temp;

	temp = nb / 2;
	i = 3;
	if (nb == 2)
		return (2);
	if (nb <= 1 || nb % 2 == 0)
		return (0);
	while (i <= temp)
	{
		if (nb % i == 0)
			return (0);
		i += 2;
	}
	return (1);
}
