#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	num[10];
	int		i;

	if (nb < 0)
	{
		nb = -(nb);
		write(1, "-", 1);
	}
	i = 0;
	if (nb == 0)
		write(1, "0", 1);
	else
	{
		while (nb != 0)
		{
			num[i++] = (nb % 10) + 48;
			nb = nb / 10;
		}
		while (i-- >= 0)
			write(1, &num[i], 1);
	}
}
