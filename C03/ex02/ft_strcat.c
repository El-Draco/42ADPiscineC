char	*ft_strcat(char *dest, char *src)
{
	int dlen;
	int i;

	dlen = 0;
	while (dest[dlen] != '\0')
		dlen++;
	i = 0;
	while (src[i] != '\0')
		dest[dlen++] = src[i++];
	dest[dlen] = '\0';
	return (dest);
}
