int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int i;
	int count;

	i = -1;
	count = 0;
	while (++i < length)
	{
		if ((*f)(tab[i]) != 0)
			count++;
	}
	return (count);
}
