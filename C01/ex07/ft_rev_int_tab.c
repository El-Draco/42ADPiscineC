void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int temp;

	i = -1;
	while (++i < size / 2)
	{
		temp = tab[i];
		tab[i] = tab[size - i - 1];
		tab[size - 1 - i] = temp;
	}
}
