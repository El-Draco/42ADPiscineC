#include <unistd.h>

void	ft_print_comb2(void)
{
	char a[5];

	a[0] = 47;
	a[2] = ' ';
	while (a[0]++ < '9')
	{
		a[1] = 47;
		while (a[1]++ < '9')
		{
			a[3] = 47;
			while (a[3]++ < '9')
			{
				a[4] = 47;
				while (a[4]++ < '9')
				{
					if (a[0] * 10 + a[1] < a[3] * 10 + a[4])
					{
						write(1, &a, 5);
						if (!(a[0] == '9' && a[1] == '8'))
							write(1, ", ", 2);
					}
				}
			}
		}
	}
}
