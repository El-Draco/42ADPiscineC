#include <unistd.h>

int		ft_str_is_numeric(char *str)
{
	int i;

	i = -1;
	if (str == NULL)
		return (1);
	while (str[++i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
			return (0);
	}
	return (1);
}
